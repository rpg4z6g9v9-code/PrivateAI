/**
 * conversationSummarizer.ts — PrivateAI Smart Summarization
 *
 * Phase 1: Auto-generates conversation summaries after each exchange.
 * Uses Claude Haiku (fast, cheap) to extract:
 * - Highlights (3-5 key points)
 * - Hard-stick notes (important decisions/findings)
 * - Action items (with status: done/pending/blocked)
 * - Files referenced
 * - Knowledge graph nodes
 * - Time spent & message count
 *
 * Stores in SQLite with user control fields (pinned, tags, keepUntil, archivedToCloud)
 */

import Constants from 'expo-constants';
import { openDatabaseAsync } from 'expo-sqlite';

const CLAUDE_HAIKU_MODEL = 'claude-haiku-4-5-20251001';
const CLAUDE_API_URL = 'https://api.anthropic.com/v1/messages';

// ─────────────────────────────────────────────────────────────
// TYPES

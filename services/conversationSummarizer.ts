export async function summarizeConversation(
  conversationId: string,
  messages: ConversationMessage[],
  apiKey: string
): Promise<ConversationSummary> {
  if (messages.length === 0) {
    throw new Error('No messages to summarize');
  }

  const summaryData = await generateSummaryData(messages, apiKey);
  const subject = detectSubject(messages);
  const kgNodes = extractKGNodes(messages);

  const summary: ConversationSummary = {
    id: `summary_${Date.now()}_${Math.random().toString(36).slice(2, 9)}`,
    conversationId,
    date: new Date().toISOString(),
    subject,
    highlights: summaryData.highlights,
    hardStickNotes: summaryData.hardStickNotes,
    actionItems: summaryData.actionItems,
    filesReferenced: summaryData.filesReferenced,
    knowledgeGraphNodes: kgNodes,
    estimatedTimeSpent: summaryData.estimatedTimeSpent,
    messageCount: messages.length,
    pinnedForReview: false,
    userTags: [],
  };

  return summary;
}


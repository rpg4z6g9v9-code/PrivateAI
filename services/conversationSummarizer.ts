export async function updateSummary(
  summaryId: string,
  patch: Partial<ConversationSummary>
): Promise<void> {
  const db = await getDatabase();
  
  const updates: string[] = [];
  const values: any[] = [];

  if (patch.pinnedForReview !== undefined) {
    updates.push('pinned = ?');
    values.push(patch.pinnedForReview ? 1 : 0);
  }
  if (patch.userTags !== undefined) {
    updates.push('user_tags = ?');
    values.push(JSON.stringify(patch.userTags));
  }
  if (patch.keepUntil !== undefined) {
    updates.push('keep_until = ?');
    values.push(patch.keepUntil);
  }
  if (patch.archivedToCloud !== undefined) {
    updates.push('archived_to_cloud = ?');
    values.push(patch.archivedToCloud);
  }

  if (updates.length === 0) return;

  values.push(summaryId);
  await db.runAsync(
    `UPDATE conversation_summaries SET ${updates.join(', ')} WHERE id = ?`,
    values
  );

  console.log('[Summarizer] Updated summary:', summaryId);
}


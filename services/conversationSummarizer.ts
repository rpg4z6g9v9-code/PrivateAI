export function detectSubject(messages: ConversationMessage[]): string {
  const text = messages.map(m => m.content).join(' ').toLowerCase();
  
  const subjects: Record<string, string[]> = {
    'PrivateAI Development': ['privateai', 'architecture', 'router', 'model', 'llama', 'integration', 'agent', 'indexing', 'summariz'],
    'Personal Notes': ['note', 'remember', 'idea', 'thought', 'personal', 'random'],
    'Research': ['research', 'explore', 'investigate', 'study', 'learn', 'understand', 'analysis'],
    'Project Work': ['project', 'build', 'implement', 'develop', 'work', 'feature', 'bug fix'],
    'Medical': ['health', 'medical', 'appointment', 'symptom', 'medication', 'doctor'],
  };
  
  let bestMatch = 'General';
  let bestScore = 0;
  
  for (const [subject, keywords] of Object.entries(subjects)) {
    const score = keywords.filter(kw => text.includes(kw)).length;
    if (score > bestScore) {
      bestMatch = subject;
      bestScore = score;
    }
  }
  
  return bestMatch;
}
// ─────────────────────────────────────────────────────────────
// HAIKU SUMMARIZATION
// ─────────────────────────────────────────────────────────────

export async function generateSummaryData(
  messages: ConversationMessage[],
  apiKey: string
): Promise<SummaryData> {
  if (!apiKey) {
    throw new Error('Claude API key not configured');
  }

  // Build conversation text
  const conversationText = messages
    .map(m => `${m.role === 'user' ? 'User' : 'Assistant'}: ${m.content}`)
    .join('\n\n');

  const systemPrompt = `You are a conversation summarizer. Respond ONLY with valid JSON. No markdown, no code blocks, no explanation. Start your response with { and end with }.`;

  const userPrompt = `Summarize this conversation as JSON with this exact structure:
{"highlights":["point1","point2","point3"],"hardStickNotes":["note1"],"actionItems":[{"task":"description","status":"pending"}],"filesReferenced":[],"estimatedTimeSpent":5}

Rules:
- highlights: 3-5 key points discussed
- hardStickNotes: critical decisions or must-remember items (can be empty array)
- actionItems: tasks with status "done", "pending", or "blocked" (can be empty array)
- filesReferenced: any filenames mentioned (can be empty array)
- estimatedTimeSpent: conversation length in minutes (number)

Conversation:
${conversationText}`;

  try {
    const res = await fetch(CLAUDE_API_URL, {
      method: 'POST',
      headers: {
        'x-api-key': apiKey,
        'anthropic-version': '2023-06-01',
        'content-type': 'application/json',
      },
      body: JSON.stringify({
        model: CLAUDE_HAIKU_MODEL,
        max_tokens: 800,
        system: systemPrompt,
        messages: [
          {
            role: 'user',
            content: userPrompt,
          },
          {
            role: 'assistant',
            content: '{',
          },
        ],
      }),
    });

    if (!res.ok) {
      const err = await res.text();
      throw new Error(`Haiku API error ${res.status}: ${err}`);
    }

    const data = await res.json();
    const rawText = data?.content?.[0]?.text ?? '';
    // Prepend the '{' we used as prefill
    const responseText = '{' + rawText;

    console.log('[Summarizer] Raw Haiku response (first 300 chars):', responseText.slice(0, 300));
    console.log('[Summarizer] Stop reason:', data?.stop_reason, 'Content blocks:', data?.content?.length);

    // Try to parse JSON from response
    let parsed: any = null;

    // Attempt 1: Extract JSON object from { to }
    try {
      let jsonStr = responseText.trim();
      jsonStr = jsonStr.replace(/^```json\s*/i, '').replace(/^```\s*/i, '').replace(/\s*```$/, '');
      const firstBrace = jsonStr.indexOf('{');
      const lastBrace = jsonStr.lastIndexOf('}');
      if (firstBrace !== -1 && lastBrace > firstBrace) {
        jsonStr = jsonStr.slice(firstBrace, lastBrace + 1);
      }
      parsed = JSON.parse(jsonStr);
    } catch (parseErr) {
      console.warn('[Summarizer] JSON parse attempt 1 failed:', (parseErr as Error).message);
    }

    // Attempt 2: If truncated, try closing the JSON manually
    if (!parsed) {
      try {
        let fixedJson = responseText.trim();
        // Remove markdown fences
        fixedJson = fixedJson.replace(/```json\s*/gi, '').replace(/```/g, '');
        // Try to close unclosed arrays/objects
        const openBrackets = (fixedJson.match(/\[/g) || []).length - (fixedJson.match(/\]/g) || []).length;
        const openBraces = (fixedJson.match(/\{/g) || []).length - (fixedJson.match(/\}/g) || []).length;
        for (let i = 0; i < openBrackets; i++) fixedJson += ']';
        for (let i = 0; i < openBraces; i++) fixedJson += '}';
        // Remove trailing comma before closing brackets
        fixedJson = fixedJson.replace(/,\s*([}\]])/g, '$1');
        parsed = JSON.parse(fixedJson);
        console.log('[Summarizer] JSON parse attempt 2 succeeded (auto-closed brackets)');
      } catch (parseErr2) {
        console.warn('[Summarizer] JSON parse attempt 2 failed:', (parseErr2 as Error).message);
      }
    }

    // Attempt 3: Extract what we can with regex
    if (!parsed) {
      console.warn('[Summarizer] All JSON parse attempts failed, extracting manually');
      const highlightMatch = responseText.match(/"highlights"\s*:\s*\[(.*?)\]/s);
      const highlights = highlightMatch
        ? highlightMatch[1].match(/"([^"]+)"/g)?.map(s => s.replace(/"/g, '')) ?? []
        : ['Conversation summary (auto-extracted)'];

      parsed = {
        highlights,
        hardStickNotes: [],
        actionItems: [],
        filesReferenced: [],
        estimatedTimeSpent: 0,
      };
    }

    return {
      highlights: parsed.highlights || [],
      hardStickNotes: parsed.hardStickNotes || [],
      actionItems: parsed.actionItems || [],
      filesReferenced: parsed.filesReferenced || [],
      estimatedTimeSpent: parsed.estimatedTimeSpent || 0,
    };
  } catch (e) {
    console.error('[Summarizer] Haiku error:', e);
    // Return empty structure on error, don't crash
    return {
      highlights: ['[Summary generation failed]'],
      hardStickNotes: [],
      actionItems: [],
      filesReferenced: [],
      estimatedTimeSpent: 0,
    };
  }
}
// ─────────────────────────────────────────────────────────────
// KNOWLEDGE GRAPH NODE EXTRACTION (Simple)
// ─────────────────────────────────────────────────────────────

function extractKGNodes(messages: ConversationMessage[]): string[] {
  // Simple extraction: look for capitalized words that might be concepts
  const text = messages.map(m => m.content).join(' ');
  
  // Find capitalized phrases (2-3 word noun phrases)
  const regex = /\b([A-Z][a-z]+(?:\s+[A-Z][a-z]+)*)\b/g;
  const matches = text.match(regex) || [];
  
  // Filter common words and deduplicate
  const stopWords = new Set(['The', 'A', 'An', 'And', 'Or', 'But', 'Is', 'Are', 'Was', 'Were']);
  const nodes = [...new Set(matches)]
    .filter(m => !stopWords.has(m) && m.length > 2)
    .slice(0, 10); // limit to 10 nodes
  
  return nodes;
}


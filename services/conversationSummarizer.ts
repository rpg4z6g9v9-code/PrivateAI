let _db: any = null;

async function getDatabase() {
  if (_db) return _db;
  try {
    _db = await openDatabaseAsync('privateai.db');
    
    // Create conversation_summaries table if not exists
    await _db.execAsync(`
      CREATE TABLE IF NOT EXISTS conversation_summaries (
        id TEXT PRIMARY KEY,
        conversation_id TEXT NOT NULL,
        date TEXT NOT NULL,
        subject TEXT,
        highlights TEXT,                 -- JSON array
        hard_stick_notes TEXT,            -- JSON array
        action_items TEXT,                -- JSON array
        files_referenced TEXT,            -- JSON array
        kg_nodes TEXT,                    -- JSON array
        time_spent INTEGER,
        message_count INTEGER,
        pinned BOOLEAN DEFAULT 0,
        user_tags TEXT,                   -- JSON array
        keep_until TEXT,
        archived_to_cloud TEXT,
        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
      );
    `);
    
    console.log('[SummaryDB] Table ready');
    return _db;
  } catch (e) {
    console.error('[SummaryDB] init error:', e);
    throw e;
  }
}

